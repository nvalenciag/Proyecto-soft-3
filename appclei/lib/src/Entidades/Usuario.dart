class Usuario {
  String nombre = "";
  String correo = "";
  String foto = "";


  
  //String rutaImagen;
  String getNombre() {
    return nombre;
  }

  void setNombre(String nombre) {
    this.nombre = nombre;
  }

  String getCorreo() {
    return correo;
  }

  void setCorreo(String correo) {
    this.correo = correo;
  }

  String getFoto() {
    return foto;
  }

  void setFoto(String foto) {
    this.foto = foto;
  }
}
